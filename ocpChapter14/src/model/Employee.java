package model;

public class Employee {

    private String name;

    private int age;

    private int salary;

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public Number getAge() {
        return age;
    }

    public void setAge(final int age) {
        this.age = age;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(final int salary) {
        this.salary = salary;
    }

}
